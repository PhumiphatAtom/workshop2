grade = int(input("Enter your score : "))

if grade <= 49:
    print("Grade: F")
elif grade <= 54:
    print("Grade: D")
elif grade <= 59:
    print("Grade: D+")
elif grade <= 64:
    print("Grade: C")
elif grade <= 69:
    print("Grade: C+")
elif grade <= 74:
    print("Grade: B")
elif grade <= 79:
    print("Grade: B+")
elif grade <= 100:
    print("Grade: A")